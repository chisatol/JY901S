void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==TIM6)
	{
		gyro_data=(float)stcGyro.w[2]/32768*200*180;
		
		gyroz_last=gyroz;
		gyroz=(gyro_data+0)*10/164;
		gyroz = gyroz * 0.9 + gyroz_last * 0.1;
		anglez+=(float)(gyroz)*0.01;

		
		if(anglez>180)
		{
			anglez=-180;
		}
			if(anglez<-180)
		{
			anglez=180;
		}
	}		
}

